<template class="container">
  <div class="register">
    <form class="form">
      <p style="color: #b3b3b3 ;">Let's create a new account!</p>
      <input type="text" placeholder="Email" v-model="email"><br>
      <input type="password" placeholder="Password" v-model="password"><br>
      <button v-on:click="signUp" type="submit">Sign up</button>
      <p class="message">Already registered? <router-link to="/login"> Sign In</router-link></p>
    </form>
  </div>
</template>
<script>
import firebase from 'firebase'
export default {
  name: 'signUp',
  data: function () {
    return {
      email: '',
      password: ''
    }
  },
  methods: {
    signUp: function () {
      firebase.auth().createUserWithEmailAndPassword(this.email, this.password).then(
        (user) => {
          this.$router.replace('login')
        },
        (err) => {
          alert('NOT OK ' + err.message)
        }
      )
    }
  }
}
</script>
